inline void playtune(const Song* song) {
  
//  int melody_len = sizeof(melody)/sizeof(melody[0]);
  // iterate over the notes of the melody:
//  for (int thisNote = 0; thisNote < melody_len; thisNote++) {
  int thisNote = 0;
  const int* notes = *(song->notes);
  const int* durations = *(song->durations);
  
  while(durations[thisNote] != 0) {
    
    // to calculate the note duration, take one second (or tempo) divided by the note type.
    //e.g. quarter note = 1000 / 4, eighth note = 1000/8, etc.
    int noteDuration = TEMPO / durations[thisNote];
    tone(MUSIC_OUT_PIN, notes[thisNote], noteDuration);

    // to distinguish the notes, set a minimum time between them.
    // the note's duration + 30% seems to work well:
    int pauseBetweenNotes = noteDuration * 1.30;
    delay(pauseBetweenNotes);
    // stop the tone playing:
    noTone(MUSIC_OUT_PIN);
    thisNote++;
  }
  
}

static bool booped = false;
static unsigned long booped_at = 0;
static unsigned long time_now = 0;
const int boop_duration = 1000 / 6; 
inline void boop(void) {
  booped_at = millis();
  booped = true;
  tone(MUSIC_OUT_PIN, NOTE_C8, boop_duration);
}

inline void checkBoop(void) {
  if(booped) {
    time_now = millis();
    if(time_now > booped_at + boop_duration) {
      booped = false;
      noTone(MUSIC_OUT_PIN);
    }
  }
}

